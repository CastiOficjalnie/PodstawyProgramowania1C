#symbole logiczne

#koniunckja
print(2 == 3 and 3 > 1)

#alternatywa
login = 'qwerty'
haslo = 'uiop'
print(login == 'admin' or haslo == 'uiop')

#zzaprzeczenie
print(not(login == 'admin' or haslo == 'uiop'))

#alternatywa wykluczajaca
fryjer = True
murarz = True
print(fryjer == False ^ murarz == True)

#priorytety operatorow logicznych
print(2 == 3 and 3 < 1 or 2 < 6)

#operatory standardowe
print(2 + 3)
print(2 - 3)
print(2 * 3)
print(2 / 3)

#potegowanie
print(2 ** 3)

#pierwiastkowanie
print(36 ** 0.5)
print(125 ** (1/3))

#dzielenie calkowite (div)
print(12 // 5)
